package org.apache.spark

import scala.reflect.ClassTag
import org.apache.spark.rdd.{PartitionPruningRDD, RDD}
import scala.collection.mutable.ArrayBuffer
import scala.collection.mutable
import scala.util.hashing._

/**
 * Expose functionality hidden in RangePartitioner.
 */
object RangePartitioning {

  /**
   * TODO refactor this in spark to allow reuse (if possible)
   * Note: Taken verbatim from: https://github.com/apache/spark/blob/master/core/src/main/scala/org/apache/spark/Partitioner.scala
   * @return An array of upper bounds for the first (partitions - 1) partitions
   */
  def rangeBounds[K : Ordering : Numeric : ClassTag, V : ClassTag](rdd: RDD[_ <: Product2[K,V]], partitions : Int): Array[K] = {
    if (partitions <= 1) {
      Array.empty
    } else {
      // This is the sample size we need to have roughly balanced output partitions, capped at 1M.
      val sampleSize = math.min(20.0 * partitions, 1e6)
      // Assume the input partitions are roughly balanced and over-sample a little bit.
      val sampleSizePerPartition = math.ceil(3.0 * sampleSize / rdd.partitions.size).toInt
      val (numItems, sketched) = RangePartitioner.sketch(rdd.map(_._1), sampleSizePerPartition)
      if (numItems == 0L) {
        Array.empty
      } else {
        // If a partition contains much more than the average number of items, we re-sample from it
        // to ensure that enough items are collected from that partition.
        val fraction = math.min(sampleSize / math.max(numItems, 1L), 1.0)
        val candidates = ArrayBuffer.empty[(K, Float)]
        val imbalancedPartitions = mutable.Set.empty[Int]
        sketched.foreach { case (idx, n, sample) =>
          if (fraction * n > sampleSizePerPartition) {
            imbalancedPartitions += idx
          } else {
            // The weight is 1 over the sampling probability.
            val weight = (n.toDouble / sample.size).toFloat
            for (key <- sample) {
              candidates += ((key, weight))
            }
          }
        }
        if (imbalancedPartitions.nonEmpty) {
          // Re-sample imbalanced partitions with the desired sampling probability.
          val imbalanced = new PartitionPruningRDD(rdd.map(_._1), imbalancedPartitions.contains)
          val seed = byteswap32(-rdd.id - 1)
          val reSampled = imbalanced.sample(withReplacement = false, fraction, seed).collect()
          val weight = (1.0 / fraction).toFloat
          candidates ++= reSampled.map(x => (x, weight))
        }
        RangePartitioner.determineBounds(candidates, partitions)
      }
    }
  }


}
